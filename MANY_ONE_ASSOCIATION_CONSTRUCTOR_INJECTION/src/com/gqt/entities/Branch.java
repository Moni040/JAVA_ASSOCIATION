package com.gqt.entities;

public class Branch 
{
	private int brID;
	private String brName;
	public Branch(int brID, String brName) 
	{
		this.brID = brID;
		this.brName = brName;
	}
	public int getBrID() {
		return brID;
	}
	public String getBrName() {
		return brName;
	}
	
	

}
